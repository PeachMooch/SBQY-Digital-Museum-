    leader: { type: Object, required: true },
    groups: { type: Array, required: true }
  },
  data(){
    return { 
      activeGroup: null,
      currentCarouselPage: 0,
      carouselTimer: null,
      teachers: [
        {
          name: 'ÊùéÂΩ¶Ëææ',
